

/*!
* \mainpage Heavy Higgs project documentation
* This program computes the process \f$ pp \rightarrow t\bar{t} \f$ taking into account the effects of additional heavy, neutral Higgs bosons in the s-channel at NLO QCD. The NLO corrections for the squared Higgs production (resonant) amplitudes and the interference terms with the non-resonant background are computed in the large \f$ m_t \f$ limit. The NLO correction to the squared amplitudes corresponding to the SM only processes (the non-resonant background) are not computed within this program. The effects of heavy, neutral Higgs bosons are relevant for example in the context of two-Higgs doublet models (MSSM) and more complicated extensions of the SM Higgs sector. The program  computes the total cross-section as well as arbitrary differential distributions.
*/


